    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // MÉTODO PARA O BOTÃO CADASTRAR AO EVENTO DE CLIQUE
        // TIPO DE VEÍCULO
        if (jRadioButton1.isSelected()){ // Opção Carro para Tipo de Veiculo
            veiculo.setTipo_veiculo("Carro");
        }
        else if (jRadioButton2.isSelected()){ // Opção Motocicleta para Tipo de Veiculo
            veiculo.setTipo_veiculo("Motocicleta");
        }
        else{
            JOptionPane.showMessageDialog(null, "Selecione um tipo de veículo válido!", "Erro", JOptionPane.INFORMATION_MESSAGE);
        }
        
        // MARCA
        // OBS: como a verificação de igualdade " == " não funcionou, tivemos que usar um método próprio do Java .equals()
        if(jTextField1.getText().equals("") || jTextField1.getText().equals(" ")){ // Verificando se o campo de texto para a marca está vazio
            JOptionPane.showMessageDialog(null, "Informe a marca do veículo", "Erro", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            veiculo.setMarca(jTextField1.getText());
        }
        
        // MODELO
        if(jTextField2.getText().equals("") || jTextField2.getText().equals(" ")){ // Verificando se o campo de texto para o modelo está vazio
            JOptionPane.showMessageDialog(null, "Informe um modelo do veículo", "Erro", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            veiculo.setModelo(jTextField2.getText());
        }
        
        // ANO DE FABRICAÇÃO
        if (jTextField3.getText().equals("") || jTextField3.getText().equals(" ")){ // Verificando se o campo de texto para o ano do veículo está vazio
            JOptionPane.showMessageDialog(null, "Informe um ano válido", "Erro", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            veiculo.setAno_fab(Integer.parseInt(jTextField3.getText()));
        }
        
        // TIPO DE COMBUSTÍVEL
        if (jRadioButton3.isSelected()){
            veiculo.setCombustivel("Flex");
        }
        else if (jRadioButton4.isSelected()){
            veiculo.setCombustivel("Gasolina");
        }
        else if (jRadioButton5.isSelected()){
            veiculo.setCombustivel("Gás");
        }
        else if (jRadioButton6.isSelected()){
            veiculo.setCombustivel("Elétrico");
        }
        else{
            JOptionPane.showMessageDialog(null, "Selecione um tipo de combustível válido!", "Erro", JOptionPane.INFORMATION_MESSAGE);
        }
        
        // PLACA
        if (jTextField4.getText().equals("") || jTextField4.getText().equals(" ")){ // Verificando se o campo de texto para a placa está vazio
            JOptionPane.showMessageDialog(null, "Informe uma placa", "Erro", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            veiculo.setPlaca(jTextField4.getText());
        }
        
        // VALOR DO VEÍCULO
        if (jTextField5.getText().equals("") || jTextField5.getText().equals(" ")){ // Verificando se o campo de texto para o valor está vazio
            JOptionPane.showMessageDialog(null, "Informe o valor do veículo", "Erro", JOptionPane.INFORMATION_MESSAGE);
        }
        else if (Double.parseDouble(jTextField5.getText()) <= 0){ // Verificando se o valor inserido foi menor ou igual a 0
            JOptionPane.showMessageDialog(null, "O valor do veículo precisa ser maior que R$0,00", "Erro", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            veiculo.setValor(Double.parseDouble(jTextField5.getText()));
        }
        
        jLabel8.setText("Status: Veículo cadastrado com sucesso!");
    }//GEN-LAST:event_jButton1ActionPerformed

