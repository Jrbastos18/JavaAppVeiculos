    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // MÉTODO PARA O BOTÃO CALCULAR IPVA AO EVENTO DE CLIQUE
        String mensagem = "";
        double ipva;
        
        if (veiculo.getMarca() == null || veiculo.getModelo() == null || veiculo.getTipo_veiculo() == null || veiculo.getCombustivel() == null || veiculo.getPlaca() == null){ // Verificando se o veículo foi cadastrado
            JOptionPane.showMessageDialog(null, "Nenhum veículo foi cadastrado", "ERRO", JOptionPane.INFORMATION_MESSAGE);
        }
        else if (veiculo.getAno_fab() <= 2005){ // Verificando o ano para calcular o IPVA
            mensagem = "\n Tipo do Veículo.......: " + veiculo.getTipo_veiculo() +
                       "\n Marca.................: " + veiculo.getMarca() +
                       "\n Modelo................: " + veiculo.getModelo() +
                       "\n Ano de Fabricação.....: " + veiculo.getAno_fab() +
                       "\n Tipo de Combustível...: " + veiculo.getCombustivel() +
                       "\n Placa.................: " + veiculo.getPlaca() +
                       "\n Valor.................: R$" + veiculo.getValor() +
                       "\n Valor do IPVA.........: VEÍCULO ISENTO DE IPVA!";
            
            JOptionPane.showMessageDialog(null, mensagem, "Resultado", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            ipva = 0;
            if (veiculo.getTipo_veiculo() == "Carro"){ // Verificando se o tipo de veículo é um carro
                if (veiculo.getCombustivel() == "Flex" || veiculo.getCombustivel() == "Gasolina") { // Verificando se o tipo de combustivel do carro é Flex ou gasolina
                    ipva = veiculo.getValor() * 0.04;
                }
                else if (veiculo.getCombustivel() == "Gás" || veiculo.getCombustivel() == "Elétrico") { // Verificando se o tipo de combustivel do carro é Gás
                    ipva = veiculo.getValor() * 0.03;
                }
            }
            else{
                ipva = veiculo.getValor() * 0.02;
            }
            
            mensagem = "\n Tipo do Veículo.......: " + veiculo.getTipo_veiculo() +
                       "\n Marca.................: " + veiculo.getMarca() +
                       "\n Modelo................: " + veiculo.getModelo() +
                       "\n Ano de Fabricação.....: " + veiculo.getAno_fab() +
                       "\n Tipo de Combustível...: " + veiculo.getCombustivel() +
                       "\n Placa.................: " + veiculo.getPlaca() +
                       "\n Valor.................: R$" + veiculo.getValor() +
                       "\n Valor do IPVA.........: R$" + ipva;
            
            JOptionPane.showMessageDialog(null, mensagem, "Resultado", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

